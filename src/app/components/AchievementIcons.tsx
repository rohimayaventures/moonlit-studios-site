export function getAchievementIcon(achievementId: string, className?: string) {
  switch (achievementId) {
    case "first-steps":
      return <FootstepsIcon className={className} />;
    case "curious-wanderer":
      return <MapIcon className={className} />;
    case "completionist":
      return <TrophyIcon className={className} />;
    case "chat-initiate":
      return <ChatBubbleIcon className={className} />;
    case "deep-conversation":
      return <SpeechIcon className={className} />;
    case "shapeshifter":
      return <MasksIcon className={className} />;
    case "spell-caster":
      return <BoltIcon className={className} />;
    case "hogwarts-graduate":
      return <GraduationCapIcon className={className} />;
    case "owl-dispatched":
      return <OwlIconAchievement className={className} />;
    case "konami-master":
      return <GamepadIcon className={className} />;
    case "avatar-state":
      return <DiamondIconAchievement className={className} />;
    case "theme-switcher":
      return <PaletteIcon className={className} />;
    case "dedicated-visitor":
      return <ClockIcon className={className} />;
    case "easter-egg-hunter":
      return <EggIcon className={className} />;
    case "moonlit-legend":
      return <MoonCrescentIcon className={className} />;
    default:
      return <TrophyIcon className={className} />;
  }
}
